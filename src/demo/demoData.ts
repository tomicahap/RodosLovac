// ─── Medium Demo (~200 persons) ──────────────────────────────

function generateMediumDemo(): string {
  const lines: string[] = ['0 HEAD', '1 SOUR PREDCI Demo Medium', '1 GEDC', '2 VERS 5.5.1', '1 CHAR UTF-8'];

  const surnames = ['Kovač', 'Horvatić', 'Blažević', 'Jurić', 'Perić', 'Novak', 'Šimić', 'Radić',
    'Toth', 'Marić', 'Filipović', 'Balen', 'Grgić', 'Vuković', 'Tomić', 'Mikulić', 'Babić',
    'Nikolić', 'Pavić', 'Lončar', 'Weber', 'Müller', 'Schmidt', 'Fischer', 'Wagner'];

  const givenM = ['Ivan', 'Josip', 'Stjepan', 'Marko', 'Franjo', 'Ante', 'Tomislav', 'Nikola',
    'Filip', 'Mijo', 'Darko', 'Luka', 'Matija', 'Petar', 'Karlo', 'Branko', 'Drago',
    'Hans', 'Friedrich', 'Wilhelm', 'Georg', 'Heinrich', 'Karl', 'Franz'];

  const givenF = ['Marija', 'Katarina', 'Ana', 'Petra', 'Milica', 'Rozika', 'Ružica', 'Kata',
    'Jelena', 'Sandra', 'Nina', 'Lucija', 'Mia', 'Ivana', 'Vesna', 'Gordana', 'Maja',
    'Maria', 'Anna', 'Elisabeth', 'Katharina', 'Margarete', 'Sophie', 'Helene'];

  const places = [
    'Zagreb, Croatia', 'Split, Croatia', 'Rijeka, Croatia', 'Osijek, Croatia',
    'Varaždin, Croatia', 'Karlovac, Croatia', 'Zadar, Croatia', 'Dubrovnik, Croatia',
    'Pula, Croatia', 'Slavonski Brod, Croatia', 'Đakovo, Croatia', 'Požega, Croatia',
    'Čakovec, Croatia', 'Koprivnica, Croatia', 'Bjelovar, Croatia', 'Sisak, Croatia',
    'Vienna, Austria', 'Graz, Austria', 'Munich, Germany', 'Berlin, Germany',
    'Ljubljana, Slovenia', 'Maribor, Slovenia', 'Sarajevo, Bosnia', 'Beograd, Serbia',
    'Buenos Aires, Argentina', 'Cleveland, Ohio, USA', 'Pittsburgh, Pennsylvania, USA',
  ];

  let personIdx = 1;
  let famIdx = 1;
  const families: Array<{ id: number; husb: number; wife: number; children: number[] }> = [];

  // Generate 4 generations of families
  function rnd<T>(arr: T[]): T { return arr[Math.floor(Math.random() * arr.length)]; }
  function rndYear(min: number, max: number) { return min + Math.floor(Math.random() * (max - min)); }

  // Gen 0 - root couple
  const rootHusbId = personIdx++;
  const rootWifeId = personIdx++;
  const rootFamId = famIdx++;

  const rootSurname = rnd(surnames);

  lines.push(`0 @I${rootHusbId}@ INDI`);
  lines.push(`1 NAME ${rnd(givenM)} /${rootSurname}/`);
  lines.push(`1 SEX M`);
  lines.push(`1 BIRT`);
  lines.push(`2 DATE ${rndYear(1970, 1985)} `);
  lines.push(`2 PLAC ${rnd(places)}`);
  lines.push(`1 FAMS @F${rootFamId}@`);

  const wifeSurname = rnd(surnames);
  lines.push(`0 @I${rootWifeId}@ INDI`);
  lines.push(`1 NAME ${rnd(givenF)} /${wifeSurname}/`);
  lines.push(`1 SEX F`);
  lines.push(`1 BIRT`);
  lines.push(`2 DATE ${rndYear(1972, 1988)} `);
  lines.push(`2 PLAC ${rnd(places)}`);
  lines.push(`1 FAMS @F${rootFamId}@`);

  const rootFamily = { id: rootFamId, husb: rootHusbId, wife: rootWifeId, children: [] as number[] };
  families.push(rootFamily);

  // Build generations
  function addGeneration(parentFamilies: typeof families, generationsLeft: number, birthYearRange: [number, number]) {
    if (generationsLeft <= 0) return;
    const nextParentFamilies: typeof families = [];

    for (const parentFam of parentFamilies) {
      // Add 2-4 children per family
      const numChildren = 2 + Math.floor(Math.random() * 3);
      for (let c = 0; c < numChildren; c++) {
        const isMale = Math.random() > 0.5;
        const childId = personIdx++;
        const childSurname = isMale ? rootSurname : rnd(surnames);
        const childName = isMale ? rnd(givenM) : rnd(givenF);
        const childBirthYear = rndYear(birthYearRange[0], birthYearRange[1]);
        const childPlace = rnd(places);

        parentFam.children.push(childId);

        lines.push(`0 @I${childId}@ INDI`);
        lines.push(`1 NAME ${childName} /${childSurname}/`);
        lines.push(`1 SEX ${isMale ? 'M' : 'F'}`);
        lines.push(`1 BIRT`);
        lines.push(`2 DATE ${childBirthYear}`);
        lines.push(`2 PLAC ${childPlace}`);
        lines.push(`1 FAMC @F${parentFam.id}@`);

        // Some children have spouses
        if (generationsLeft > 1 && Math.random() > 0.3) {
          const spouseId = personIdx++;
          const spouseSurname = rnd(surnames);
          const spouseName = isMale ? rnd(givenF) : rnd(givenM);
          const spouseBirthYear = childBirthYear + rndYear(-3, 4);

          lines.push(`0 @I${spouseId}@ INDI`);
          lines.push(`1 NAME ${spouseName} /${spouseSurname}/`);
          lines.push(`1 SEX ${isMale ? 'F' : 'M'}`);
          lines.push(`1 BIRT`);
          lines.push(`2 DATE ${spouseBirthYear}`);
          lines.push(`2 PLAC ${rnd(places)}`);

          const newFamId = famIdx++;
          lines.push(`1 FAMS @F${newFamId}@`);

          // Update child with FAMS
          // (We'll add FAMS to child lines at the end)
          const childFamLine = `1 FAMS @F${newFamId}@`;
          lines.push(`0 @I${childId}@ INDI`); // Will be duplicate — handled by parser last-wins
          lines.push(`1 FAMS @F${newFamId}@`);

          const newFam = {
            id: newFamId,
            husb: isMale ? childId : spouseId,
            wife: isMale ? spouseId : childId,
            children: [],
          };
          families.push(newFam);
          nextParentFamilies.push(newFam);
        }
      }
    }

    const nextBirthYear: [number, number] = [birthYearRange[0] - 28, birthYearRange[1] - 22];
    addGeneration(nextParentFamilies, generationsLeft - 1, nextBirthYear);
  }

  // Add parent generations (going back in time)
  function addAncestors(family: typeof families[0], gen: number, maxGen: number, startYear: number) {
    if (gen >= maxGen) return;
    const fatherSurname = rnd(surnames);
    const fatherId = personIdx++;
    const motherId = personIdx++;
    const parentFamId = famIdx++;
    const birthYear = startYear - rndYear(25, 35);

    lines.push(`0 @I${fatherId}@ INDI`);
    lines.push(`1 NAME ${rnd(givenM)} /${fatherSurname}/`);
    lines.push(`1 SEX M`);
    lines.push(`1 BIRT`);
    lines.push(`2 DATE ${birthYear}`);
    lines.push(`2 PLAC ${rnd(places)}`);
    if (birthYear < 1950) {
      lines.push(`1 DEAT`);
      lines.push(`2 DATE ${birthYear + rndYear(60, 85)}`);
      lines.push(`2 PLAC ${rnd(places)}`);
    }
    lines.push(`1 FAMS @F${parentFamId}@`);

    lines.push(`0 @I${motherId}@ INDI`);
    lines.push(`1 NAME ${rnd(givenF)} /${rnd(surnames)}/`);
    lines.push(`1 SEX F`);
    lines.push(`1 BIRT`);
    lines.push(`2 DATE ${birthYear + rndYear(-3, 4)}`);
    lines.push(`2 PLAC ${rnd(places)}`);
    if (birthYear < 1950) {
      lines.push(`1 DEAT`);
      lines.push(`2 DATE ${birthYear + rndYear(55, 80)}`);
      lines.push(`2 PLAC ${rnd(places)}`);
    }
    lines.push(`1 FAMS @F${parentFamId}@`);

    // Link husband of this family to parent family
    lines.push(`0 @I${family.husb}@ INDI`);
    lines.push(`1 FAMC @F${parentFamId}@`);

    const parentFamObj = { id: parentFamId, husb: fatherId, wife: motherId, children: [family.husb] };
    families.push(parentFamObj);

    addAncestors(parentFamObj, gen + 1, maxGen, birthYear);
  }

  addAncestors(rootFamily, 0, 4, 1978);

  // Add children / descendants
  addGeneration([rootFamily], 2, [2005, 2020]);

  // Write all FAM records
  for (const fam of families) {
    lines.push(`0 @F${fam.id}@ FAM`);
    lines.push(`1 HUSB @I${fam.husb}@`);
    lines.push(`1 WIFE @I${fam.wife}@`);
    for (const child of fam.children) {
      lines.push(`1 CHIL @I${child}@`);
    }
    const marYear = rndYear(1940, 2010);
    lines.push(`1 MARR`);
    lines.push(`2 DATE ${marYear}`);
    lines.push(`2 PLAC ${rnd(places)}`);
  }

  lines.push('0 TRLR');
  return lines.join('\n');
}

